package jan16;

public class LoopsDemo {
    public static void main(String[] args) {

        int counter = 1;
        while(counter <= 5) {  // 1 <=5 ............ 5 <=5, 6<=5
            System.out.println("Hello World"); // 5 times
            counter ++;
        }

        int counter1 = 1;
        while(counter1 <= 100) {  // 1 <=5 ............ 5 <=5, 6<=5
            System.out.println(counter1); // 5 times
            counter1 ++;
        }
    }
}
